    public void dataelecteur() {
        String[] champ = {"Nom", "Prenom", "Sexe", "Telephone", "Email", "CNI", "Bureau", "Code_Vote"};
        String[] afficher = new String[9];
        DefaultTableModel model = new DefaultTableModel(null, champ);
        try {
            Connect();
            pst = con.prepareStatement("""
                                       SELECT * 
                                       FROM electeur 
                                       JOIN bureau 
                                       ON electeur.code_bureau = bureau.code_bureau""");
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                afficher[0] = rs.getString("nom_pers");
                afficher[1] = rs.getString("prenom_pers");
                afficher[2] = rs.getString("sexe_pers");
                afficher[3] = rs.getString("tel_pers");
                afficher[4] = rs.getString("email_pers");
                afficher[5] = rs.getString("cni_electeur");
                afficher[6] = rs.getString("label_bureau");
                afficher[7] = rs.getString("Code_Vote");
                model.addRow(afficher);
            }
            table.setModel(model);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
//Afficher les regions

    public void afficherregion() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Label");
        Tableregion.setModel(model);
        List<Region> regions = RegionControlleur.get();

        if (!regions.isEmpty()) {
            for (Region region : regions) {

                model.addRow(new Object[]{region.getCodeRegion(), region.getLabelRegion()});
            }
        }
    }
//Afficher les bureau

    public void afficherbureau() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Label");
        model.addColumn("Quartier");
        Tablebureau.setModel(model);
        List<Bureau> bu = ControlleurBureau.get();

        if (!bu.isEmpty()) {
            for (Bureau b : bu) {

                model.addRow(new Object[]{b.getCodeBureau(), b.getLabelBureau(), b.getQtierBureau()});
            }
        }
    }
