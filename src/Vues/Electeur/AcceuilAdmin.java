    public void affichercandidat() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Nom");
        model.addColumn("Prenom");
        model.addColumn("Email");
        model.addColumn("Date_Naiss");
        model.addColumn("Sexe");
        model.addColumn("Tel");
        model.addColumn("CNI");
        model.addColumn("Profession");
        model.addColumn("Bulletin");
        model.addColumn("Partie");
        model.addColumn("CodeVote");
        tablecandidat.setModel(model);
        List<Candidat> c = CandidatControlleur.get();
        if (!c.isEmpty()) {
            for (Candidat ca : c) {

                model.addRow(new Object[]{ca.getNomPers(), ca.getPersonne(), ca.getEmailPers(), ca.getDateNaiss(), ca.getSexePers(), ca.getTelPers(), ca.getCniCand(), ca.getProffession(), ca.getBulletin(), ca.getPartiCand(), ca.getCodeVote()});
            }
        }

    }
